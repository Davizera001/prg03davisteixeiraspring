    private void carregarTabelaFiltro(String texto) { // método para carregar tabela com filtro

    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblCursos.getModel(); // pega modelo

    modelo.setRowCount(0); // limpa tabela

    java.util.List<br.com.ifba.curso.entity.Curso> lista = new br.com.ifba.curso.dao.CursoDAO().buscarPorNome(texto); // busca filtrada

    for (br.com.ifba.curso.entity.Curso c : lista) { // percorre resultados

        modelo.addRow(new Object[]{ // adiciona na tabela
            c.getId(),
            c.getNome(),
            c.getDescricao(),
            c.getCargaHoraria()
        });
    }
}
    
    private void carregarTabela() { // método para preencher a tabela

    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblCursos.getModel(); // pega modelo da tabela

    modelo.setRowCount(0); // limpa tabela

    java.util.List<br.com.ifba.curso.entity.Curso> lista = new br.com.ifba.curso.dao.CursoDAO().listar(); // busca do banco

    for (br.com.ifba.curso.entity.Curso c : lista) { // percorre lista

        modelo.addRow(new Object[]{ // adiciona linha
            c.getId(), // id
            c.getNome(), // nome
            c.getDescricao(), // descrição
            c.getCargaHoraria() // carga
        });
    }
}
    
