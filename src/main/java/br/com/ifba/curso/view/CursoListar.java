    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
         
        int linha = tblCursos.getSelectedRow(); // obtém a linha selecionada

    if (linha >= 0) { // verifica se existe seleção

        int confirmacao = javax.swing.JOptionPane.showConfirmDialog( // abre caixa de confirmação
                this, // referência da tela atual
                "Tem certeza que deseja remover este curso?", // mensagem
                "Confirmação", // título
                javax.swing.JOptionPane.YES_NO_OPTION // opções
        ); // fim da confirmação

        if (confirmacao == javax.swing.JOptionPane.YES_OPTION) { // verifica se clicou em sim

            Long id = Long.parseLong(tblCursos.getValueAt(linha, 0).toString()); // obtém o ID

            new br.com.ifba.curso.dao.CursoDAO().remover(id); // remove o curso do banco

            carregarTabela(); // atualiza a tabela

            javax.swing.JOptionPane.showMessageDialog( // exibe mensagem de sucesso
                    this, // referência da tela
                    "Curso removido com sucesso!", // texto
                    "Sucesso", // título
                    javax.swing.JOptionPane.INFORMATION_MESSAGE // tipo
            ); // fim da mensagem

        } // fim do if interno

    } else { // caso nenhuma linha selecionada

        javax.swing.JOptionPane.showMessageDialog( // exibe aviso
                this, // referência da tela
                "Selecione um curso para remover!", // mensagem
                "Aviso", // título
                javax.swing.JOptionPane.WARNING_MESSAGE // tipo
        ); // fim da mensagem

    } // fim do else
    
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    int linha = tblCursos.getSelectedRow(); // obtém a linha selecionada na tabela

    if (linha >= 0) { // verifica se alguma linha foi selecionada

        Long id = Long.parseLong(tblCursos.getValueAt(linha, 0).toString()); // obtém o ID da linha selecionada

        br.com.ifba.curso.entity.Curso curso = null; // cria variável para armazenar o curso

        for (br.com.ifba.curso.entity.Curso c : new br.com.ifba.curso.dao.CursoDAO().listar()) { // percorre todos os cursos do banco
            if (c.getId().equals(id)) { // verifica se o ID corresponde
                curso = c; // armazena o curso encontrado
                break; // sai do loop
            } // fim do if
        } // fim do for

        CursoSave tela = new CursoSave(curso); // cria a tela de edição com os dados

        tela.setVisible(true); // exibe a tela

    } else { // caso nenhuma linha tenha sido selecionada

        javax.swing.JOptionPane.showMessageDialog( // exibe mensagem de aviso
                this, // referência da tela atual
                "Selecione um curso para editar!", // texto da mensagem
                "Aviso", // título
                javax.swing.JOptionPane.WARNING_MESSAGE // tipo de mensagem
        ); // fim da mensagem

    } // fim do else
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
    String texto = txtBusca.getText(); // pega o texto digitado

    if (texto == null || texto.trim().isEmpty()) { // verifica se está vazio

        carregarTabela(); // carrega tudo se não digitou nada

    } else {

        carregarTabelaFiltro(texto); // aplica filtro
    }
    }//GEN-LAST:event_btnPesquisarActionPerformed

